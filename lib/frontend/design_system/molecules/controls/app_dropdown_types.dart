/// Color style for [AppDropdown].
///
/// - [outline] — bordered box with surface background.
/// - [filled] — solid color background; pass [AppDropdown.color] to set the fill.
enum AppDropdownStyle { outline, filled, flat }

/// Layout variant for [AppDropdown].
///
/// - [plain] — box only, no header above.
/// - [labelOnly] — text label header above the box.
/// - [iconLabel] — icon + text label header above the box.
enum AppDropdownVariant { plain, labelOnly, iconLabel }
